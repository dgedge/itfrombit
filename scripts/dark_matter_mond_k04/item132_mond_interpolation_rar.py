# cubic/AQUAL (2/3): F(z) = int_0^z mu(sqrt(s)) ds  ->  (2/3) z^{3/2} as z->0  (the lambda_R4=(2/3)x coeff)
def cubic_coeff(mu, z=1e-6, n=20000):
    s = np.linspace(0.0, z, n)
    y = mu(np.sqrt(s))
    F = np.sum((y[1:] + y[:-1]) / 2 * np.diff(s))   # trapezoid (numpy-version-independent)
    return F / z ** 1.5
