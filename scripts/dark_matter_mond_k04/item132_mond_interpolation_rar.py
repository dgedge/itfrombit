    g_bar_p = mu(x) * g_obs_p
    log_gobs_frame = np.interp(np.log10(GBAR), np.log10(g_bar_p), np.log10(g_obs_p))
    d = log_gobs_frame - np.log10(g_obs_RAR(GBAR, a0_rar))
    return float(np.sqrt(np.mean(d ** 2))), float(np.max(np.abs(d)))

