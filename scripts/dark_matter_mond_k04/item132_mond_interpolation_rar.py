# ----------------------------------------------------------------------------- [3] RAR map + diff
def g_obs_RAR(g_bar, a0=A0_RAR):
    return g_bar / (1.0 - np.exp(-np.sqrt(g_bar / a0)))

GBAR = np.logspace(-12.0, -8.0, 600)        # SPARC baryonic-acceleration range (m/s^2)
def residual_dex(mu, a0_frame, a0_rar=A0_RAR):
    # framework parametric curve g_bar(x), g_obs(x), x=g_obs/a0 (both monotonic in x)
    x = np.logspace(-4.0, 4.0, 8000)
