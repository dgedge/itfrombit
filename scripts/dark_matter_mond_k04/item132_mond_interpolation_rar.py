# ----------------------------------------------------------------------------- [0] constants
c = 2.99792458e8                       # m/s
MPC = 3.0856775814913673e22            # m
def a0_of_H0(H0_km_s_Mpc):             # canon: a0 = c H0 / (2 pi)
    return c * (H0_km_s_Mpc * 1e3 / MPC) / (2 * np.pi)
A0_PLANCK = a0_of_H0(67.4)             # Planck 2018
A0_SHOES = a0_of_H0(73.0)              # SH0ES
A0_RAR = 1.20e-10                      # McGaugh, Lelli & Schombert 2016 (m/s^2)
A0_RAR_SYS = 0.24e-10                  # ~20% systematic on g_dagger
RAR_SCATTER = 0.11                     # intrinsic scatter (dex); ~0.13 total observed
