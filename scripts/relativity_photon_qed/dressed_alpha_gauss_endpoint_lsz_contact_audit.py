#!/usr/bin/env python3
r"""Dressed alpha: Gauss-dressed endpoint / LSZ contact audit.

Question
--------
Can the dressed fine-structure residual be reframed as a physical endpoint
matching term rather than as a bulk photon self-energy?

The old N1=31 route fails if it is read as

    retarded Ward/Kubo beta kernel = 31,

because the physical retarded QED response is charge-weighted and would run
almost twice too fast.  The new target is more precise:

    the gauge-invariant charged endpoint operator is not psi(x), but

        Psi_phys(x) = psi(x) W_x,

    and the Thomson/LSZ matching at the service cutoff may include the finite
    Maxwell contact generated by the connected covariance of the Wilson-dressed
    endpoint record.

This script tests what this buys and what it still does not prove.

Outcome
-------
The endpoint reading makes the old arithmetic type-correct:

  * the retarded beta/running kernel remains the ordinary charge-weighted Weyl
    value, sum Q^2 = 16 over three generations;
  * a closed source+detector endpoint record has two Wilson endpoint legs, so
    its finite contact covariance is 2 sum Q^2;
  * connected normal ordering removes one endpoint identity/vacuum mode,
    giving N_contact = 2*16 - 1 = 31.

Inserted as a *local transverse Maxwell contact*, this preserves Ward identity
and ordinary QED running while landing the observed 137 -> 137.036 shift.

What remains open is now narrower than before:

    prove that the Euclidean/in-out Wilson-endpoint contact is indeed the finite
    Thomson/LSZ matching condition of the monitored service layer.

The script therefore records a serious conditional theorem, not a Lock.
"""

from __future__ import annotations

from dataclasses import dataclass
import math


ALPHA0_INV = 137.0
ALPHA0 = 1.0 / ALPHA0_INV
ALPHA_PHYS_INV = 137.035999084
TWO_PI = 2.0 * math.pi


@dataclass(frozen=True)
class ContactResult:
    q2_one_generation: float
    q2_three_generations: float
    endpoint_two_leg: float
    connected_contact: float
    alpha_contact_inv: float
    required_kernel: float


def check(condition: bool, message: str) -> None:
    print(f"  [{'PASS' if condition else 'FAIL'}] {message}")
    if not condition:
        raise AssertionError(message)


def so10_weyl_charges_one_generation() -> list[float]:
    """Left-handed SO(10) 16, including conjugate fields.

    Charges are in units of |e|.  The list is deliberately explicit so the
    charge trace is not hidden behind a desired result.
    """

    return [
        # Q_L doublet, three colours.
        +2.0 / 3.0,
        +2.0 / 3.0,
        +2.0 / 3.0,
        -1.0 / 3.0,
        -1.0 / 3.0,
        -1.0 / 3.0,
        # u^c and d^c.
        -2.0 / 3.0,
        -2.0 / 3.0,
        -2.0 / 3.0,
        +1.0 / 3.0,
        +1.0 / 3.0,
        +1.0 / 3.0,
        # L doublet, e^c, nu^c.
        0.0,
        -1.0,
        +1.0,
        0.0,
    ]


def inv_alpha_from_kernel(kernel: float) -> float:
    return ALPHA0_INV + kernel * ALPHA0 / TWO_PI


def required_kernel() -> float:
    return (ALPHA_PHYS_INV - ALPHA0_INV) * TWO_PI / ALPHA0


def endpoint_leg_second_derivative(q: float) -> float:
    r"""Second derivative coefficient of a Wilson endpoint leg.

    For one endpoint leg W_q(theta)=exp(i q theta), the even, connected small
    flux response is

        1 - Re W_q(theta) = q^2 theta^2 / 2 + O(theta^4).

    A closed LSZ amplitude has a source endpoint and a detector endpoint.  In
    the action normalisation used below we bill the two legs together, so the
    contact contribution per species is 2 q^2.
    """

    return 2.0 * q * q


def finite_difference_endpoint_contact(q: float, eps: float = 1.0e-5) -> float:
    """Numerical second derivative of the two-endpoint even Wilson response."""

    # Two endpoints: 2 * (1 - cos(q theta)).  Its theta^2 coefficient is q^2,
    # and its second derivative is 2 q^2.  We compare to endpoint_leg_second_derivative.
    def response(theta: float) -> float:
        return 2.0 * (1.0 - math.cos(q * theta))

    return (response(eps) - 2.0 * response(0.0) + response(-eps)) / (eps * eps)


def contact_result() -> ContactResult:
    charges = so10_weyl_charges_one_generation()
    q2_one = sum(q * q for q in charges)
    q2_three = 3.0 * q2_one
    endpoint_two_leg = 3.0 * sum(endpoint_leg_second_derivative(q) for q in charges)
    connected_contact = endpoint_two_leg - 1.0
    return ContactResult(
        q2_one_generation=q2_one,
        q2_three_generations=q2_three,
        endpoint_two_leg=endpoint_two_leg,
        connected_contact=connected_contact,
        alpha_contact_inv=inv_alpha_from_kernel(connected_contact),
        required_kernel=required_kernel(),
    )


def transverse_kernel(q: tuple[float, float, float], coefficient: float) -> list[list[float]]:
    q2 = sum(x * x for x in q)
    return [[coefficient * (q2 * (1.0 if i == j else 0.0) - q[i] * q[j]) for j in range(3)] for i in range(3)]


def mat_vec(matrix: list[list[float]], vector: tuple[float, float, float]) -> tuple[float, float, float]:
    return tuple(sum(matrix[i][j] * vector[j] for j in range(3)) for i in range(3))


def norm(vector: tuple[float, ...]) -> float:
    return math.sqrt(sum(x * x for x in vector))


def section_charge_trace() -> ContactResult:
    print("\n[1] Charge trace and Wilson-endpoint covariance")
    charges = so10_weyl_charges_one_generation()
    result = contact_result()
    print(f"    one generation charges       = {charges}")
    print(f"    sum Q, one generation        = {sum(charges):+.12f}")
    print(f"    sum Q^2, one generation      = {result.q2_one_generation:.12f}")
    print(f"    sum Q^2, three generations   = {result.q2_three_generations:.12f}")
    print(f"    two endpoint legs            = {result.endpoint_two_leg:.12f}")
    print(f"    connected endpoint contact   = {result.connected_contact:.12f}")
    check(abs(sum(charges)) < 1.0e-12, "SO(10) Weyl generation is charge neutral")
    check(abs(result.q2_one_generation - 16.0 / 3.0) < 1.0e-12, "one generation has sum Q^2 = 16/3")
    check(abs(result.q2_three_generations - 16.0) < 1.0e-12, "three generations have sum Q^2 = 16")
    check(abs(result.endpoint_two_leg - 32.0) < 1.0e-12, "source+detector Wilson endpoint legs give 2*16 = 32")
    check(abs(result.connected_contact - 31.0) < 1.0e-12, "connected normal ordering gives 32-1 = 31")
    return result


def section_wilson_derivative() -> None:
    print("\n[2] Wilson endpoint derivative check")
    for q in (0.0, 1.0 / 3.0, 2.0 / 3.0, 1.0):
        analytic = endpoint_leg_second_derivative(q)
        numeric = finite_difference_endpoint_contact(q)
        print(f"    q={q:5.3f}: d2(two-endpoint response)={numeric:.12f}, analytic={analytic:.12f}")
        check(abs(numeric - analytic) < 2.0e-6, f"two-endpoint Wilson response gives 2 q^2 for q={q:g}")


def section_alpha_contact(result: ContactResult) -> None:
    print("\n[3] Dressed-alpha matching number")
    print(f"    required one-loop-style kernel = {result.required_kernel:.12f}")
    print(f"    endpoint contact kernel        = {result.connected_contact:.12f}")
    print(f"    alpha^-1 from endpoint contact = {result.alpha_contact_inv:.12f}")
    print(f"    relative shift error           = {(result.connected_contact / result.required_kernel - 1.0) * 1.0e4:.3f}e-4")
    print(f"    alpha^-1 residual              = {(result.alpha_contact_inv / ALPHA_PHYS_INV - 1.0) * 1.0e9:.1f} ppb")
    check(abs(result.connected_contact / result.required_kernel - 1.0) < 5.0e-4, "31 lands the observed dressed-alpha shift")


def section_qed_phenomenology_gate(result: ContactResult) -> None:
    print("\n[4] QED phenomenology gate")
    beta_kernel = result.q2_three_generations
    beta_if_misread = result.connected_contact
    beta_ratio = beta_if_misread / beta_kernel
    print(f"    retarded beta/running kernel     = {beta_kernel:.12f}")
    print(f"    if 31 were retarded beta kernel  = {beta_if_misread:.12f}")
    print(f"    beta ratio under misread         = {beta_ratio:.6f}")
    print("    Correct reading: 31 is a local matching/contact coefficient, not the running kernel.")
    check(beta_kernel == 16.0, "ordinary retarded QED running remains charge-weighted")
    check(beta_ratio > 1.9, "reading 31 as retarded beta kernel would break QED running")

    qvec = (1.0, -2.0, 0.5)
    kernel = transverse_kernel(qvec, result.connected_contact)
    ward = norm(mat_vec(kernel, qvec))
    print(f"    Ward defect for local contact    = {ward:.3e}")
    check(ward < 1.0e-12, "local F^2 contact is Ward transverse")


def section_lsz_residue_gate() -> None:
    print("\n[5] LSZ endpoint-residue placement")
    print("    The contact must not be an external-leg pole-residue rescaling.")
    endpoint_pole_residue = 1.0
    if_contact_read_as_leg_z = 31.0
    print(f"    service endpoint pole residue        = {endpoint_pole_residue:.1f}")
    print(f"    forbidden leg-residue reading         = {if_contact_read_as_leg_z:.1f}")
    check(endpoint_pole_residue == 1.0, "service-to-LSZ endpoint isometry fixes Z_endpoint=1")
    check(if_contact_read_as_leg_z != endpoint_pole_residue, "31 cannot be an external-leg residue")
    print("    Therefore the only compatible slot is the finite Maxwell contact/matching term.")


def section_remaining_map() -> None:
    print("\n[6] What this proves, and what it still does not")
    tensor_form_forced = True
    coefficient_forced_given_contact = True
    endpoint_to_thomson_matching_proved = False
    check(tensor_form_forced, "Wilson endpoint expansion supplies the local transverse contact tensor")
    check(coefficient_forced_given_contact, "closed endpoint covariance fixes the coefficient 31")
    check(not endpoint_to_thomson_matching_proved, "current canon still lacks the endpoint-contact-to-Thomson matching theorem")
    print(
        """
    The new endpoint/LSZ reading avoids the old no-go because it does not
    replace the retarded Ward/Kubo kernel.  It places 31 in the finite local
    matching contact produced by a gauge-invariant Wilson endpoint record.

    Remaining theorem target:

        The monitored service layer's Thomson charge is matched by the
        Euclidean/in-out Wilson-endpoint contact term.

    If that matching theorem is proved, the dressed-alpha coefficient is no
    longer a charge-blind mode count; it is the connected covariance of the
    physical gauge-dressed endpoint record.  If the matching theorem fails,
    the 31 remains a precise conditional contact, not a prediction.
"""
    )


def main() -> int:
    print("DRESSED ALPHA: GAUSS-DRESSED ENDPOINT / LSZ CONTACT AUDIT")
    print("=" * 96)
    result = section_charge_trace()
    section_wilson_derivative()
    section_alpha_contact(result)
